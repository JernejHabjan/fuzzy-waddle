create or replace view "public"."fly_squasher_scores_with_user_meta" with (security_invoker=on) as  WITH scores AS (
         SELECT fss.id,
            fss.score,
            fss.level,
            fss.user_id,
            fss.date,
            pr.name,
            row_number() OVER (PARTITION BY fss.level ORDER BY fss.score DESC) AS level_rn
           FROM (( SELECT fly_squasher_scores.id,
                    fly_squasher_scores.score,
                    fly_squasher_scores.level,
                    fly_squasher_scores.user_id,
                    fly_squasher_scores.date,
                    dense_rank() OVER (PARTITION BY fly_squasher_scores.level, fly_squasher_scores.user_id ORDER BY fly_squasher_scores.score DESC) AS rn
                   FROM public.fly_squasher_scores) fss
             JOIN public.profiles pr ON ((fss.user_id = pr.id)))
          WHERE (fss.rn = 1)
        )
 SELECT id,
    score,
    level,
    user_id,
    date,
    name,
    level_rn
   FROM scores
  WHERE (level_rn <= 3);


CREATE OR REPLACE FUNCTION public.get_player_score_metrics(p_player_score_id bigint)
 RETURNS jsonb
 LANGUAGE sql
 STABLE
AS $function$
SELECT jsonb_object_agg(mt.metric_key, m.metric_value)
FROM probable_waffle_player_score_metrics m
       JOIN probable_waffle_score_metric_types mt ON m.metric_type_id = mt.id
WHERE m.player_score_id = p_player_score_id;
$function$
;

CREATE OR REPLACE FUNCTION public.handle_new_user()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO ''
AS $function$
BEGIN

  -- if provider is Google
  IF NEW.raw_app_meta_data ->> 'provider' = 'google' THEN
    INSERT INTO public.profiles (id, email, name, profile_image_url, created)
    VALUES (NEW.id, NEW.email, NEW.raw_user_meta_data ->> 'name', NEW.raw_user_meta_data ->> 'avatar_url', CURRENT_TIMESTAMP);
  ELSE
    -- else insert random name
    INSERT INTO public.profiles (id, email, name, profile_image_url, created)
    VALUES (NEW.id,
            NEW.email,
            substring(
              string_agg(chr(65 + floor(random() * 26)::int), ''), -- Random letters (A-Z)
              1, 10 -- 10-character random name
            ),
            '',
            CURRENT_TIMESTAMP);
  END IF;

  RETURN NEW;
END;
$function$
;

create or replace view "public"."little_muncher_scores_with_user_meta" with (security_invoker=on) as  WITH ranked_scores AS (
         SELECT s.id,
            s.score,
            s.hill,
            s.user_id,
            s.date,
            p.name AS user_name,
            row_number() OVER (PARTITION BY s.hill, s.user_id ORDER BY s.score DESC) AS user_rn
           FROM (public.little_muncher_scores s
             JOIN public.profiles p ON ((s.user_id = p.id)))
        ), unique_user_scores AS (
         SELECT ranked_scores.id,
            ranked_scores.score,
            ranked_scores.hill,
            ranked_scores.user_id,
            ranked_scores.date,
            ranked_scores.user_name,
            ranked_scores.user_rn
           FROM ranked_scores
          WHERE (ranked_scores.user_rn = 1)
        ), hill_ranked AS (
         SELECT unique_user_scores.id,
            unique_user_scores.score,
            unique_user_scores.hill,
            unique_user_scores.user_id,
            unique_user_scores.user_name,
            unique_user_scores.date,
            row_number() OVER (PARTITION BY unique_user_scores.hill ORDER BY unique_user_scores.score DESC) AS hill_rn
           FROM unique_user_scores
        )
 SELECT id,
    score,
    hill,
    user_id,
    user_name,
    date
   FROM hill_ranked
  WHERE (hill_rn <= 3)
  ORDER BY hill, hill_rn;


create or replace view "public"."probable_waffle_match_history" with (security_invoker=on) as  SELECT gs.id,
    gs.game_instance_id,
    gs.game_type,
    gs.map_id,
    gs.session_state,
    gs.started_at,
    gs.ended_at,
    gs.total_duration_seconds,
    gs.scores_submitted,
    gs.human_player_count,
    creator.name AS created_by_name,
    submitter.name AS submitted_by_name,
    ( SELECT json_agg(json_build_object('player_number', ps.player_number, 'player_name', ps.player_name, 'player_type', ps.player_type, 'faction_type', ps.faction_type, 'game_result', ps.game_result, 'final_score', ps.final_score, 'is_current_user', (ps.user_id = auth.uid())) ORDER BY ps.final_score DESC) AS json_agg
           FROM public.probable_waffle_player_scores ps
          WHERE (ps.game_session_id = gs.id)) AS players,
    (EXISTS ( SELECT 1
           FROM public.probable_waffle_player_scores ps
          WHERE ((ps.game_session_id = gs.id) AND (ps.user_id = auth.uid())))) AS user_participated,
    ( SELECT ps.game_result
           FROM public.probable_waffle_player_scores ps
          WHERE ((ps.game_session_id = gs.id) AND (ps.user_id = auth.uid()))
         LIMIT 1) AS user_result
   FROM ((public.probable_waffle_game_sessions gs
     LEFT JOIN public.profiles creator ON ((gs.created_by_user_id = creator.id)))
     LEFT JOIN public.profiles submitter ON ((gs.scores_submitted_by = submitter.id)))
  WHERE ((gs.scores_submitted = true) AND (EXISTS ( SELECT 1
           FROM public.probable_waffle_player_scores ps
          WHERE ((ps.game_session_id = gs.id) AND (ps.user_id = auth.uid())))))
  ORDER BY gs.ended_at DESC NULLS LAST, gs.started_at DESC;


