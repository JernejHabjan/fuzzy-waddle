     LEFT JOIN public.probable_waffle_player_score_metrics m ON ((ps.id = m.player_score_id)))
     LEFT JOIN public.probable_waffle_score_metric_types mt ON ((m.metric_type_id = mt.id)))
  GROUP BY ps.id, ps.game_session_id, ps.user_id, ps.player_number, ps.player_name, ps.player_type, ps.team_number, ps.faction_type, ps.game_result, ps.eliminated, ps.eliminated_at, ps.final_score, ps.created_at;


create or replace view "public"."probable_waffle_player_stats" with (security_invoker=on) as  SELECT ps.user_id,
    p.name AS player_name,
    count(*) AS total_games,
    count(*) FILTER (WHERE (ps.game_result = 'win'::text)) AS wins,
    count(*) FILTER (WHERE (ps.game_result = 'loss'::text)) AS losses,
    count(*) FILTER (WHERE (ps.game_result = 'tie'::text)) AS ties,
    round((((count(*) FILTER (WHERE (ps.game_result = 'win'::text)))::numeric / (NULLIF(count(*) FILTER (WHERE (ps.game_result <> 'quit'::text)), 0))::numeric) * (100)::numeric), 2) AS win_rate_percentage,
    sum(ps.units_produced) AS total_units_produced,
    sum(ps.units_killed) AS total_units_killed,
    sum(ps.buildings_constructed) AS total_buildings_constructed,
    sum(ps.buildings_destroyed) AS total_buildings_destroyed,
    (avg(ps.final_score))::integer AS avg_final_score,
    max(ps.final_score) AS max_final_score
   FROM (public.probable_waffle_player_scores_full ps
     LEFT JOIN public.profiles p ON ((ps.user_id = p.id)))
  WHERE (ps.user_id IS NOT NULL)
  GROUP BY ps.user_id, p.name;


